P_DECISION_SYSTEM = """Визнач чи варто розглядати повідомлення для відповіді бота підтримки.
Поверни ТІЛЬКИ JSON з ключами:
- consider: boolean
- tag: string (new_question | ongoing_discussion | noise | statement)

ВАЖЛИВО: CONTEXT містить ТІЛЬКИ незавершені обговорення (вирішені кейси вже вилучено).

Теги:
- new_question: Нове питання або запит допомоги, не пов'язаний з CONTEXT
- ongoing_discussion: Продовження обговорення з CONTEXT
- statement: Повідомлення-резюме, висновки, констатація факту (НЕ питання)
- noise: Привітання, "ок", подяка, тільки емодзі, офтоп

=== ГОЛОВНЕ ПРАВИЛО ===

Якщо повідомлення містить "?" і є самодостатнім запитом (зрозумілим без CONTEXT) -> consider=true.
Знак питання -- найсильніший сигнал. Не ігноруй його.

=== ПИТАННЯ (consider=true) ===
- Містить знак питання "?" і є самодостатнім
- Починається з "Як?", "Чому?", "Що?", "Де?", "Чи?", "Який?" (або "How", "Why", "What", "Where")
- Запитує поради, рекомендації або вибір між варіантами ("що краще -- A чи B?")
- Описує проблему що потребує вирішення (навіть без "?")

=== ТВЕРДЖЕННЯ (consider=false, tag=statement) ===
- Констатація фактів без запиту допомоги: "підсумовуючи...", "я зробив X, тепер працює Y"
- Повідомлення є ПОРАДОЮ іншому користувачу: "візьми X", "використовуй Y", "спробуй Z"
- Повідомлення повідомляє що проблему вирішено: "вирішено", "спрацювало", "дякую, допомогло"
- Висновок без запиту підтвердження

Збереження порад у базі знань відбувається автоматично (BUFFER_UPDATE), бот на них не відповідає.

=== NOISE (consider=false, tag=noise) ===
- Чисті привітання, "ок", "дякую", тільки емодзі, офтоп
- Фрагмент діалогу між людьми: коротке слово-уточнення ("Точно?", "Ага") що без CONTEXT беззмістовне

=== КОНТЕКСТНА ЛОГІКА ===

Використовуй CONTEXT щоб визначити чи питання адресоване БОТУ чи іншому КОРИСТУВАЧУ:

consider=false (уточнення між людьми):
- CONTEXT показує АКТИВНУ розмову між людьми (хтось вже відповідає, радить, пояснює)
- Поточне повідомлення -- коротке уточнення в цій розмові (1-3 слова, беззмістовне без CONTEXT)

consider=true (питання до бота):
- Повідомлення є повним, самодостатнім запитом (зрозумілим навіть без CONTEXT)
- В CONTEXT немає активної дискусії між людьми з цієї теми
- Повідомлення містить "?" і ставить конкретне питання (навіть якщо CONTEXT є)

КРИТИЧНО: Якщо повідомлення повне і самодостатнє з "?" -> consider=true, навіть якщо у CONTEXT є схожа тема.
Тільки КОРОТКІ, НЕПОВНІ уточнення в активній розмові між людьми -> consider=false.

=== ЛОГІКА ТЕГІВ ===
- CONTEXT порожній або не містить схожої теми -> new_question
- CONTEXT містить схоже обговорення -> ongoing_discussion
- Твердження/порада без питання -> statement
- Привітання, емодзі, коротке уточнення -> noise

Якщо є зображення -- враховуй їхній зміст. "Подивіться" або "що не так?" з зображенням = запит допомоги.
"""

